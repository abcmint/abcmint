bool CNode::IsBanned(CNetAddr ip) {
    bool fResult = false;
    {
        LOCK(cs_setBanned);
        std::map<CNetAddr, int64>::iterator i = setBanned.find(ip);
        if (i != setBanned.end()) {
            int64 t = (*i).second;
            if (GetTime() < t) fResult = true;
        }
    }
    return fResult;
}

bool CNode::Misbehaving(int howmuch) {
    if (addr.IsLocal()) {
        printf("Warning: Local node %s misbehaving (delta: %d)!\n",
               addrName.c_str(), howmuch);
        return false;
    }

    nMisbehavior += howmuch;
    if (nMisbehavior >= GetArg("-banscore", 100)) {
        int64 banTime =
            GetTime() +
            GetArg("-bantime", 60 * 60 * 24);  // Default 24-hour ban
        printf("Misbehaving: %s (%d -> %d) DISCONNECTING\n",
               addr.ToString().c_str(), nMisbehavior - howmuch, nMisbehavior);
        {
            LOCK(cs_setBanned);
            if (setBanned[addr] < banTime) setBanned[addr] = banTime;
        }
        CloseSocketDisconnect();
        return true;
    } else
        printf("Misbehaving: %s (%d -> %d)\n", addr.ToString().c_str(),
               nMisbehavior - howmuch, nMisbehavior);
    return false;
}

