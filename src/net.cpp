CNode* ConnectNode(CAddress addrConnect, const char* pszDest) {
    if (pszDest == NULL) {
        if (IsLocal(addrConnect)) return NULL;

        // Look for an existing connection
        CNode* pnode = FindNode((CService)addrConnect);
        if (pnode) {
            pnode->AddRef();
            return pnode;
        }
    }

    /// debug print
    printf(
        "trying connection %s lastseen=%.1fhrs\n",
        pszDest ? pszDest : addrConnect.ToString().c_str(),
        pszDest ? 0 : (double)(GetAdjustedTime() - addrConnect.nTime) / 3600.0);

    // Connect
    SOCKET hSocket;
    if (pszDest ? ConnectSocketByName(addrConnect, hSocket, pszDest,
                                      GetDefaultPort())
                : ConnectSocket(addrConnect, hSocket)) {
        addrman.Attempt(addrConnect);

        /// debug print
        printf("connected %s\n",
               pszDest ? pszDest : addrConnect.ToString().c_str());

        // Set to non-blocking
#ifdef WIN32
        u_long nOne = 1;
        if (ioctlsocket(hSocket, FIONBIO, &nOne) == SOCKET_ERROR)
            printf(
                "ConnectSocket() : ioctlsocket non-blocking setting failed, "
                "error %d\n",
                WSAGetLastError());
#else
        if (fcntl(hSocket, F_SETFL, O_NONBLOCK) == SOCKET_ERROR)
            printf(
                "ConnectSocket() : fcntl non-blocking setting failed, error "
                "%d\n",
                errno);
#endif

        // Add node
        CNode* pnode =
            new CNode(hSocket, addrConnect, pszDest ? pszDest : "", false);
        pnode->AddRef();

        {
            LOCK(cs_vNodes);
            vNodes.push_back(pnode);
        }

        pnode->nTimeConnected = GetTime();
        return pnode;
    } else {
        return NULL;
    }
}

void CNode::CloseSocketDisconnect() {
    fDisconnect = true;
    if (hSocket != INVALID_SOCKET) {
        printf("disconnecting node %s\n", addrName.c_str());
        closesocket(hSocket);
        hSocket = INVALID_SOCKET;
    }

    // in case this fails, we'll empty the recv buffer when the CNode is deleted
    TRY_LOCK(cs_vRecvMsg, lockRecv);
    if (lockRecv) vRecvMsg.clear();

    // if this was the sync node, we'll need a new one
    if (this == pnodeSync) pnodeSync = NULL;
}

